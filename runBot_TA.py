def convert_to_number(s):
    multipliers = {
        'K': 1_000,
        'k': 1_000,
        'M': 1_000_000,
        'B': 1_000_000_000,
        'T': 1_000_000_000_000
    }
    if len(s) > 1 and s[-1] in multipliers:
        try:
            num_part = float(s[:-1])
            multiplier = multipliers[s[-1]]
            return str(int(num_part * multiplier))
        except ValueError:
            return s
    else:
        return s


def make_id(title: str, brand: str):
    samp_id = ''

    for word in brand.split(' '):
        if (len(word) > 0) and (len(word) < 3):
            samp_id += word
        else:
            samp_id += word[0:3]

    samp_id += '_'

    for word in title.split(' '):
        if (len(word) > 0) and (len(word) < 3):
            samp_id += word
        else:
            samp_id += word[0:3]

    return samp_id

